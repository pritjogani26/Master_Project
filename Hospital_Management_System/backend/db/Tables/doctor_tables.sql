    leaving time without time zone,
    created_at timestamp with time zone NOT NULL,
    schedule_id integer NOT NULL,
    lunch_end time without time zone,
    lunch_start time without time zone,
    CONSTRAINT doctor_working_days_pkey PRIMARY KEY (id),
    CONSTRAINT doctor_working_days_schedule_id_day_of_week_3a07f362_uniq UNIQUE (schedule_id, day_of_week),
    CONSTRAINT doctor_working_days_schedule_id_67dbb916_fk_doctor_sc FOREIGN KEY (schedule_id)
        REFERENCES public.doctor_schedules (schedule_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
        DEFERRABLE INITIALLY DEFERRED,
    CONSTRAINT doctor_working_days_arrival_leaving_check CHECK (arrival IS NULL OR leaving IS NULL OR arrival < leaving),
    CONSTRAINT doctor_working_days_day_of_week_check CHECK (day_of_week >= 0 AND day_of_week <= 6)
);




