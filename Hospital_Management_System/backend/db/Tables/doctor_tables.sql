CREATE TABLE IF NOT EXISTS public.doctors
(
    doctor_id uuid NOT NULL,
    full_name character varying(255) COLLATE pg_catalog."default" NOT NULL,
    experience_years numeric(6,2) NOT NULL,
    phone_number character varying(15) COLLATE pg_catalog."default" NOT NULL,
    consultation_fee numeric(10,2),
    registration_number character varying(100) COLLATE pg_catalog."default" NOT NULL,
    profile_image character varying(255) COLLATE pg_catalog."default" NOT NULL,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    gender_id integer,
    verification_status character varying(20) COLLATE pg_catalog."default" NOT NULL,
    verification_notes text COLLATE pg_catalog."default",
    verified_at timestamp with time zone,
    verified_by_id uuid,
    CONSTRAINT doctors_pkey PRIMARY KEY (doctor_id),
    CONSTRAINT doctors_gender_id_9b833a22_fk_genders_gender_id FOREIGN KEY (gender_id)
        REFERENCES public.genders (gender_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
        DEFERRABLE INITIALLY DEFERRED,
    CONSTRAINT doctors_user_id_480f6e79_fk_users_user_id FOREIGN KEY (doctor_id)
        REFERENCES public.users (user_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
        DEFERRABLE INITIALLY DEFERRED,
    CONSTRAINT doctors_verified_by_id_659a9c03_fk_users_user_id FOREIGN KEY (verified_by_id)
        REFERENCES public.users (user_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
        DEFERRABLE INITIALLY DEFERRED
);


