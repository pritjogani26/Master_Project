CREATE TABLE IF NOT EXISTS public.user_roles
(
    role_id serial primary key,
    role character varying(30) COLLATE pg_catalog."default" NOT NULL,
    role_description text COLLATE pg_catalog."default",

    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),

    created_by uuid REFERENCES users(user_id),
    updated_by uuid REFERENCES users(user_id),
	
    CONSTRAINT user_roles_name_key UNIQUE (role)
);


drop table permissions;
drop table role_permissions;

create table if not EXISTS permissions (
    permission_id int generated by DEFAULT as identity primary key,
    module varchar(50) not null,
    action varchar(50) not null,
    description varchar(255),

    created_at timestamp DEFAULT now(),
    updated_at timestamp,

    UNIQUE(module, action)
);


-- Your original DDL has a missing comma before UNIQUE — apply this fix:
CREATE TABLE IF NOT EXISTS role_permissions (
    role_id         INT         NOT NULL REFERENCES user_roles(role_id) ON DELETE CASCADE,
    permission_id   INT         NOT NULL REFERENCES permissions(permission_id) ON DELETE CASCADE,
    grant_by        UUID        REFERENCES users(user_id),
    grant_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT role_permissions_pkey PRIMARY KEY (role_id, permission_id)
);

