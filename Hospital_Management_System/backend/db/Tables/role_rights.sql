    SELECT
        r.role_id,
        p.permission_id
    FROM assignments a
    JOIN user_roles  r ON r.role   = a.role_name
    JOIN permissions p ON p.module = a.module
                      AND p.action = a.action
    ON CONFLICT (role_id, permission_id) DO NOTHING;

    RAISE NOTICE 'Default permissions assigned successfully.';

END $$;